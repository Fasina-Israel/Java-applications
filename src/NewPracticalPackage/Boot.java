package NewPracticalPackage;

public class Boot {
    private String name;
    private int number;
    private String rug;

    public Boot(String name, int number, String rug) {
        this.name = name;
        this.number = number;
        this.rug = rug;
    }

    public String getName() {
        return name;
    }

    public int getNumber() {
        return number;
    }

    public String getRug() {
        return rug;
    }
}
